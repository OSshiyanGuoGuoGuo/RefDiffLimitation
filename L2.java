public class ReptileAndFish {

    public static class Turtle {
        private String name;

        public Reptile(String name) {
            this.name = name;
        }

        public String getName() {
            return this.name;
        }
    }

    public static class Shark {
        private String name;

        public Fish(String name) {
            this.name = name;
        }

        public String getName() {
            return this.name;
        }
    }

}